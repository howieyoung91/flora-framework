package xyz.yanghaoyu.flora.exception;

public class NestedTransactionNotSupportedException extends TransactionException {
    public NestedTransactionNotSupportedException() {
    }

    public NestedTransactionNotSupportedException(String message) {
        super(message);
    }

    public NestedTransactionNotSupportedException(String message, Throwable cause) {
        super(message, cause);
    }

    public NestedTransactionNotSupportedException(Throwable cause) {
        super(cause);
    }

    public NestedTransactionNotSupportedException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
